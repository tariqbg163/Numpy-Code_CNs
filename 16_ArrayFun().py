# specialy method  of array

import numpy as np
ar=np.array([[11,22,33],[44,55,66],[77,88,99],[11,44,44]])
print("Min Value = ", ar.min())
print("Max Value = ", ar.max())
print("Mean Value = ", ar.mean())
print("Sum of all values = ", ar.sum())
print("Index of min value = ", ar.argmin())
print("Index of max value = ", ar.argmax())